package structure;

public class SwitchDemo01 {
    public static void main(String[] args) {
        String name = "Messi";

        switch (name){
            case "Messi":
                System.out.println("leo");
                break;
            case "John":
                System.out.println("gg");
                break;
            case"Lois":
                System.out.println("xixi");
                break;
            default:
                System.out.println("haha");
        }
    }
}
